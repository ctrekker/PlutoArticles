## 3D Glasses - Theory

Fundamentally the lenses in 3D glasses are designed such that a different image can be delivered to each eye, allowing a technique called [stereoscopy](https://en.wikipedia.org/wiki/Stereoscopy) to be applied, which tricks the brain into seeing depth in a 2D scene.

Older (or cheaper) 3D glasses achieve this through colored filters, with a particular color channel being percieved by each eye.
