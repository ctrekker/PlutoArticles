# ╔═╡ 90ea4b8b-b2b1-46d8-a9f1-c67fd27756e6
md"""
We begin by defining some filter types.

- A `LinearPolarizerFilter` will linearly polarize light to a specific angle $\theta$.


- An `IdealWavePlateFilter` will shift the phase of the light by $\phi$ with respect to the linear basis of angle $\theta$. Being ideal means this phase shift is irrespective of wavelength


- A `WavePlateFilter` is the same as `IdealWavePlateFilter`, but rather replicates the behavior of a birefringent material with indices of refraction differing by $\Delta n$ along the specified orientation $\theta$. Combined with some length $L$, a phase shift effect is present, but it is dependent on the incident light's wavelength.
"""

# ╔═╡ 1bbbd662-2b33-442a-b117-09b099d60a90
abstract type AbstractFilter end

# ╔═╡ 98f3a117-c3ec-4360-8b7b-dc89bd09e4cd
struct LinearPolarizerFilter <: AbstractFilter
	θ::Float64  # orientation
end

# ╔═╡ 9b1b460e-dae3-11ec-2c5d-bbaf605c21b2
struct IdealWavePlateFilter <: AbstractFilter
	θ::Float64  # orientation
	ϕ::Float64  # phase shift (radians)
end

# ╔═╡ 3550e60e-6b68-4de1-8db7-fb9f528676bb
begin
	struct WavePlateFilter <: AbstractFilter
		θ::Float64  # orientation
		Δn::Float64 # difference in refractive indices along x and y axes
		L::Float64  # lens length
	end
	ϕ(f::WavePlateFilter, λ::Float64) = (2π * f.Δn * f.L) / λ
	nothing
end

# ╔═╡ 85e76dc2-3409-4a56-9ba5-a7b698d9f400
struct CompoundFilter <: AbstractFilter
	filters::Vector{AbstractFilter}
end

# ╔═╡ 99c83d81-2165-4a3e-a5b7-4edd25dcf111
md"""
Next we define `Light`, which will keep track of the electric field magnitudes and corresponding phases along each axis, along with its wavelength. For ideal filters the wavelength $\lambda$ is unimportant, but later we'll see it plays a small (but non-negligible) role in real waveplate filters.
"""

# ╔═╡ 59cf4207-4196-4c2a-9465-9fa5a4633b90
begin
	struct Light
		E::Vector{Float64}  # amplitude of the electric field oscillations
		ϕ::Vector{Float64}  # phase shift of electric field oscillations
		λ::Float64
	end
	function Light(E::Vector{Float64})
		Light(E, [0., 0.], 0.)
	end
	function Light(E::Vector{Float64}, ϕ::Vector{Float64})
		Light(E, ϕ, 0.)
	end
	nothing
end

# ╔═╡ 829c817b-3fd7-4d01-8e9d-0e4cdecb64f6
rot(θ) = [
	cos(θ)  sin(θ);
	-sin(θ) cos(θ)
]

# ╔═╡ 39905383-33f6-4f4a-8181-6eee679ff039
md"""
Each of the `filter_light` methods will take input light and return a new light object with parameters modified in a way consistent with the physics of the provided filter. The physics is left unexplained as it's a bit outside the scope of this article.
"""

# ╔═╡ 793b15a4-c5ae-4858-a70a-f0e3b91d6ead
begin
	function filter_light(filter::LinearPolarizerFilter, light::Light)
		# linear polarization is more complex than I originally thought when dealing with arbitrary circular or elliptical orbits
		# we use a change of basis operation to simplify the trig
		lEϕ = rot(filter.θ) * [light.E[1] * exp(im*light.ϕ[1]), light.E[2] * exp(im*light.ϕ[2])]
		mag = abs(lEϕ[1])
		E = [
			mag * cos(filter.θ),
			mag * sin(filter.θ)
		]
		
		Light(E, [0., 0.], light.λ)
	end
	function filter_light(filter::IdealWavePlateFilter, light::Light)
		θ, ϕₛ, = filter.θ, filter.ϕ
		E, ϕ = light.E, light.ϕ
		
		# copied from "Circular Polarization.jl"
		coeff = [
			E[1]*exp(im*ϕ[1]) * (exp(im*ϕₛ)*cos(θ)^2 + sin(θ)^2)  +  E[2]*exp(im*ϕ[2]) * cos(θ)*sin(θ) * (exp(im*ϕₛ) - 1),
			E[2]*exp(im*ϕ[2]) * (exp(im*ϕₛ)*sin(θ)^2 + cos(θ)^2)  +  E[1]*exp(im*ϕ[1]) * cos(θ)*sin(θ) * (exp(im*ϕₛ) - 1)
		]
		E′ = abs.(coeff)
		ϕ′ = angle.(coeff)
		
		# since the filter is ideal
		Light(E′, ϕ′, light.λ)
	end
	function filter_light(filter::WavePlateFilter, light::Light)
		# using the ϕ equation for wave-plate filters, we can convert a wave plate filter into an ideal wave plate filter given a specific incident wavelength
		ideal_equivalent = IdealWavePlateFilter(filter.θ, ϕ(filter, light.λ))
		filter_light(ideal_equivalent, light)
	end
	function filter_light(filter::CompoundFilter, light::Light)
		current_light = light
		for f ∈ filter.filters
			current_light = filter_light(f, current_light)
		end
		current_light
	end
end

