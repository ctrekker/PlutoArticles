md"""
There's an issue though: to filter circularly polarized light, these glasses will use two different filters. The first filter, closest to the eye, is the familiar **linear polarizer**. If you've ever used sunglasses, chances are that these are linearly polarizing lenses. You can check by looking at a computer screen with the sunglasses on and rotating your head back and forth. The screen should fade between dim and bright, and at some angle you should be able to completely eliminate *all* light. We'll see why this is later with a simulation.

The second filter is a **waveplate filter**. The function of this filter is a bit technical, but for the sake of these glasses, all they do is turn circularly polarized light *into* linearly polarized light. This way the circularly polarized image can be filtered easily with a linear polarizer, producing the desired 2-image effect necessary for stereoscopy!
