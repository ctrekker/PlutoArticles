# ╔═╡ 3f8992ab-d069-46d7-a910-bd13b21d2786
function _compute_period(E, ϕ; steps=100)
	r = 0:(2π/steps):(2π+2π/steps)
	[E[i]*cos(t+ϕ[i]) for t ∈ r, i ∈ 1:length(E)]
end

# ╔═╡ 9c4c7461-956d-4664-82ca-625b60782890
function polarization_plot(light::Light)
	trail = _compute_period(light.E, light.ϕ)
	plot(trail[:, 1], trail[:, 2]; xlims=(-1, 1), ylims=(-1, 1), size=(250, 250), legend=false, linewidth=2, color=:red)
	plot!(trail[1:5, 1], trail[1:5, 2], color=:black, linewidth=3, arrow=true)
end

